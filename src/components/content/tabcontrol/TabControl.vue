<template>
    <div class="tab-control">  
        <div class="tab-control-item" v-for="(item,index) in titles" :key="index"
         :class="{active:index===currentIndex}" @click="itemClick(index)">
            <span>{{item}}</span>
        </div>
        
         </div>
</template>


<script>
export default {
    name:'TabControl',
    props:{
        titles:{
            type:Array,
            default(){
                return[]
            }
        }
    },
    data(){
        return{
            currentIndex:0,
        }
    },
    methods:{
        itemClick(index){
            this.currentIndex=index
            this.$emit('tabClick',index)//把时间传给夫标签
        }
    }
}
</script>
<style scoped>
.tab-control{
    display: flex;
    padding: 15px 0;
    background-color: #fff;
}
.tab-control-item{
    flex: 1;
    text-align: center;
    line-height: 15px;
}
.active span{
    color: #3eae58;
    border-bottom: 3px solid #3eae58;
    padding: 5px;
}
</style>